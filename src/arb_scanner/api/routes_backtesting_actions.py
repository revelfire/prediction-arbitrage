"""API routes for backtesting action triggers (analyze, report, sweep, apply)."""

from __future__ import annotations

import asyncio
import os
import sys
from datetime import UTC, datetime, timedelta
from pathlib import Path
from typing import Any

import structlog
import yaml
from fastapi import APIRouter, Depends, HTTPException, Request, UploadFile
from pydantic import BaseModel

from arb_scanner.api.deps import get_backtest_repo, get_config, get_flip_repo
from arb_scanner.backtesting.automation import build_backtest_report_data, run_import_workflow
from arb_scanner.models.config import Settings

logger: structlog.stdlib.BoundLogger = structlog.get_logger(
    module="api.backtesting_actions",
)
router = APIRouter(prefix="/api/backtesting", tags=["backtesting"])

_ALLOWED_CONFIG_PREFIXES = (
    "flippening.categories.",
    "auto_execution.",
)


class SweepRequest(BaseModel):
    """Request body for parameter sweep."""

    category: str
    param: str
    min: float
    max: float
    step: float


class ConfigSuggestionBody(BaseModel):
    """Single config assignment generated by backtesting."""

    config_path: str
    suggested_value: Any


class ApplySuggestionsBody(BaseModel):
    """Request body for persisting suggestions to config.yaml."""

    suggestions: list[ConfigSuggestionBody]
    restart: bool = True


@router.post("/import-and-run")
async def import_and_run_backtesting(
    file: UploadFile,
    config: Settings = Depends(get_config),
    repo: Any = Depends(get_backtest_repo),
    flip_repo: Any = Depends(get_flip_repo),
) -> dict[str, Any]:
    """Import a CSV and immediately rebuild report data plus config suggestions."""
    try:
        content = await file.read()
        return await run_import_workflow(
            content,
            config=config,
            repo=repo,
            flip_repo=flip_repo,
        )
    except Exception as exc:
        logger.error("import_and_run_failed", error=str(exc))
        raise HTTPException(400, f"Import failed: {exc}") from exc


@router.post("/analyze")
async def analyze_portfolio(
    category: str | None = None,
    repo: Any = Depends(get_backtest_repo),
    flip_repo: Any = Depends(get_flip_repo),
) -> dict[str, Any]:
    """Reconstruct positions, compute portfolio and category performance."""
    try:
        report = await build_backtest_report_data(repo, flip_repo, category=category)
        for perf in report["category_models"]:
            await repo.upsert_category_performance(perf)
        for pos in report["positions"]:
            await repo.upsert_position(pos)
        trades = report["trades"]
        trade_count = sum(1 for trade in trades if trade.action.value in ("Buy", "Sell"))
        return {
            "portfolio": report["portfolio"],
            "category_performance": report["category_performance"],
            "trade_count": trade_count,
        }
    except Exception as exc:
        logger.error("analyze_failed", error=str(exc))
        raise HTTPException(500, f"Analysis failed: {exc}") from exc


@router.post("/report")
async def run_backtest_report(
    category: str | None = None,
    since: str | None = None,
    until: str | None = None,
    repo: Any = Depends(get_backtest_repo),
    flip_repo: Any = Depends(get_flip_repo),
) -> dict[str, Any]:
    """Run full backtest report with signal comparison."""
    try:
        since_dt = datetime.fromisoformat(since) if since else None
        until_dt = datetime.fromisoformat(until) if until else None
        report = await build_backtest_report_data(
            repo,
            flip_repo,
            category=category,
            since=since_dt,
            until=until_dt,
        )
        for perf in report["category_models"]:
            await repo.upsert_category_performance(perf)
        return {
            "portfolio": report["portfolio"],
            "signal_alignment": report["signal_alignment"],
            "category_performance": report["category_performance"],
        }
    except Exception as exc:
        logger.error("report_failed", error=str(exc))
        raise HTTPException(500, f"Report failed: {exc}") from exc


@router.post("/config/apply")
async def apply_backtest_config_suggestions(
    body: ApplySuggestionsBody,
    request: Request,
    config: Settings = Depends(get_config),
) -> dict[str, Any]:
    """Write one or more backtest-generated config suggestions to disk."""
    if not body.suggestions:
        raise HTTPException(400, "No suggestions supplied")

    cfg_path = _resolve_config_path(request, config)
    path = Path(cfg_path)
    try:
        raw = yaml.safe_load(path.read_text(encoding="utf-8")) or {}
    except FileNotFoundError as exc:
        raise HTTPException(404, f"Config file not found: {cfg_path}") from exc
    except Exception as exc:
        raise HTTPException(500, f"Failed to read config: {exc}") from exc

    if not isinstance(raw, dict):
        raise HTTPException(500, "Config file must contain a top-level mapping")

    applied: list[dict[str, Any]] = []
    for suggestion in body.suggestions:
        _validate_config_path(suggestion.config_path)
        _set_nested_value(raw, suggestion.config_path.split("."), suggestion.suggested_value)
        applied.append(
            {
                "config_path": suggestion.config_path,
                "suggested_value": suggestion.suggested_value,
            }
        )

    try:
        path.write_text(
            yaml.safe_dump(raw, sort_keys=False),
            encoding="utf-8",
        )
    except Exception as exc:
        raise HTTPException(500, f"Failed to write config: {exc}") from exc

    if body.restart:
        asyncio.create_task(_restart_process(request))

    logger.info(
        "backtest_config_applied",
        config_path=cfg_path,
        applied=len(applied),
        restart=body.restart,
    )
    return {
        "status": "ok",
        "config_path": cfg_path,
        "applied": applied,
        "restart_scheduled": body.restart,
    }


@router.post("/sweep")
async def run_sweep_endpoint(
    body: SweepRequest,
    config: Settings = Depends(get_config),
    repo: Any = Depends(get_backtest_repo),
) -> dict[str, Any]:
    """Run a parameter sweep and persist the best result."""
    from arb_scanner.cli._replay_helpers import run_sweep
    from arb_scanner.models.backtesting import OptimalParamSnapshot

    try:
        now = datetime.now(tz=UTC)
        since = now - timedelta(days=30)
        result = await run_sweep(
            config,
            body.category,
            since,
            now,
            body.param,
            body.min,
            body.max,
            body.step,
        )
        entries: list[Any] = result.get("results", [])
        if entries:
            best = max(entries, key=lambda r: r[1].get("win_rate", 0))
            snapshot = OptimalParamSnapshot(
                category=body.category,
                param_name=body.param,
                optimal_value=best[0],
                win_rate_at_optimal=best[1].get("win_rate", 0),
                sweep_date=now,
            )
            await repo.upsert_optimal_param(snapshot)
        return result
    except Exception as exc:
        logger.error("sweep_failed", error=str(exc))
        raise HTTPException(500, f"Sweep failed: {exc}") from exc


def _resolve_config_path(request: Request, config: Settings) -> str:
    """Resolve the config.yaml path for suggestion persistence."""
    state_path = getattr(request.app.state, "config_path", None)
    env_path = os.environ.get("ARB_SCANNER_CONFIG")
    cfg_path = state_path or env_path or getattr(config, "_config_path", None)
    if not cfg_path:
        raise HTTPException(400, "No config file path available for persistence")
    return str(cfg_path)


def _validate_config_path(config_path: str) -> None:
    """Reject suggestion paths outside the supported config surfaces."""
    if not any(config_path.startswith(prefix) for prefix in _ALLOWED_CONFIG_PREFIXES):
        raise HTTPException(400, f"Unsupported config path: {config_path}")


def _set_nested_value(raw: dict[str, Any], path_parts: list[str], value: Any) -> None:
    """Create intermediate mappings and assign a terminal value."""
    node = raw
    for part in path_parts[:-1]:
        current = node.get(part)
        if not isinstance(current, dict):
            current = {}
            node[part] = current
        node = current
    node[path_parts[-1]] = value


async def _restart_process(request: Request) -> None:
    """Re-exec the dashboard process after the HTTP response is sent."""
    await asyncio.sleep(0.25)
    try:
        app = request.app
        config: Settings = app.state.config
        argv = [
            sys.executable,
            "-m",
            "arb_scanner",
            "serve",
            "--host",
            config.dashboard.host,
            "--port",
            str(config.dashboard.port),
        ]
        if getattr(app.state, "no_db", False):
            argv.append("--no-db")
        if getattr(app.state, "flip_watch", False):
            argv.append("--flip-watch")

        env = os.environ.copy()
        config_path = getattr(app.state, "config_path", None)
        if config_path:
            env["ARB_SCANNER_CONFIG"] = str(config_path)

        logger.warning("dashboard_restarting", argv=argv)
        os.execvpe(sys.executable, argv, env)
    except Exception:
        logger.exception("dashboard_restart_failed")
